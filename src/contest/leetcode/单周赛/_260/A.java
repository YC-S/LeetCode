  public int maximumDifference(int[] nums) {
    int n = nums.length;
    int maxDifference = -1;
    for (int k = 0; k < n; k++) {
      int min = Integer.MAX_VALUE;
      int max = Integer.MIN_VALUE;
      for (int i = 0; i < k; i++) {
        min = Math.min(min, nums[i]);
      }
      for (int j = k; j < n; j++) {
        max = Math.max(max, nums[j]);
      }
      if (max - min <= 0) {
        continue;
      } else {
        maxDifference = Math.max(maxDifference, max - min);
      }
    }
    return maxDifference;
  }
