public class Example47 {

    static class Bitwise {

        public static void main(String[] args) throws Exception {
            int a = 0x3;
            int b = 0x5;
            System.out.println(a);
            System.out.println(b);
            System.out.println(~a);
            System.out.println(~b);
            System.out.println(a & b);
            System.out.println(a ^ b);
            System.out.println(a | b);
        }
    }

}
