  public static boolean validMountainArray(int[] A) {
    if (A == null || A.length <= 2) {
      return false;
    }
    int i = 0, j = A.length - 1;
    boolean ifMove = false;
    while (i < j) {
      if (A[i] < A[i + 1]) {
        i++;
        ifMove = true;
      }
      if (A[j] < A[j - 1]) {
        j--;
        ifMove = true;
      }
      if (A[i] == A[j] && i != j) {
        return false;
      }

      if (!ifMove) {
        break;
      }
      ifMove = false;

    }
    return i == j && j != A.length - 1 && i != 0;
  }

